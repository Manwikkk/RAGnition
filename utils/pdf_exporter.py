# MCQ / True-False / All Types — Clean Question Paper with Answers
# ═══════════════════════════════════════════════════════════════════════════════

def generate_questions_pdf(
    questions: list[dict[str, Any]],
    question_type: str,
    difficulty: str,
) -> bytes:
    """
    Clean question paper PDF with correct answers marked.
    Shows: Question, Options (with correct one highlighted), Answer, Explanation.
    Does NOT include user's quiz answers — this is a study document.
    """
    pdf = _BasePDF()
    pdf.add_page()
    pdf.put_title(
        f"{question_type} Questions",
        f"Difficulty: {difficulty}  |  {len(questions)} Question(s)"
    )

    for i, q in enumerate(questions):
        correct  = str(q.get("answer", "")).strip()
        expl     = q.get("explanation", "")
        options  = q.get("options") or []

        # Question
        pdf.put_text(f"Q{i + 1}. {q.get('question', '')}", bold=True)
        pdf.ln(1)

        if options:
            for opt in options:
                opt_s = str(opt).strip()
                # Check if this option is the correct answer
                is_correct_opt = (
                    opt_s[:2].upper() == correct[:2].upper()
                    or correct.upper() in opt_s.upper()
                    or opt_s.upper() == correct.upper()
                )
                if is_correct_opt:
                    pdf.set_x(L_M + 6)
                    pdf.set_font("Helvetica", "B", 10)
                    pdf.set_text_color(0, 130, 0)
                    pdf.multi_cell(pdf.ew - 6, 6, f"* {_safe(opt_s)}  [CORRECT]")
                    pdf.set_text_color(30, 30, 30)
                else:
                    pdf.put_text(f"   {opt_s}", indent=6, color=(70, 70, 70))
        else:
            # Short / Long / Case — just show the answer
            pdf.set_x(L_M + 6)
            pdf.set_font("Helvetica", "B", 10)
            pdf.set_text_color(0, 130, 0)
            pdf.multi_cell(pdf.ew - 6, 6, f"Answer: {_safe(correct)}")
            pdf.set_text_color(30, 30, 30)

        if expl:
            pdf.ln(1)
            pdf.put_note(f"Explanation: {expl}", color=(60, 60, 60))

        pdf.ln(4)
        pdf.put_hr()

    return bytes(pdf.output())


