# 3. Mock Test Paper PDF — questions only, no answers
# ═══════════════════════════════════════════════════════════════════════════════

def generate_mock_paper_pdf(exam: dict[str, Any]) -> bytes:
    pdf = _PDF()
    pdf.add_page()
    pdf.title_block(
        exam.get("title", "Mock Examination Paper"),
        f"Total Marks: {exam.get('total_marks', 0)}"
    )

    section_letter = "A"
    for section in exam.get("sections", []):
        q_type     = section["type"]
        marks_each = section["marks_each"]
        sec_total  = section["total_marks"]

        pdf.section_header(
            f"SECTION {section_letter}  —  {q_type.upper()}"
            f"   [{marks_each} mark(s) each  |  Section Total: {sec_total} marks]"
        )

        for i, q in enumerate(section.get("questions", []), 1):
            pdf.body(f"Q{i}. {q.get('question','')}", bold=True)
            pdf.ln(2)
            for letter, text in _opts_list(q.get("options")):
                pdf.indent(f"   {letter}. {text}", color=(60, 60, 60))
            pdf.ln(5)

        pdf.ln(3)
        section_letter = chr(ord(section_letter) + 1)

    return bytes(pdf.output())


