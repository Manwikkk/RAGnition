# MCQ Interactive Quiz Result PDF
# ═══════════════════════════════════════════════════════════════════════════════

def generate_quiz_result_pdf(
    questions: list[dict[str, Any]],
    user_answers: dict[int, str],
    score: int,
    total: int,
) -> bytes:
    """
    Quiz result PDF showing: correct answer, whether user was right/wrong, explanation.
    Used after submitting MCQ / True-False quiz.
    """
    pdf = _BasePDF()
    pdf.add_page()
    pct = int(score / total * 100) if total else 0
    pdf.put_title("Quiz Results", f"Score: {score} / {total}  ({pct}%)")

    for i, q in enumerate(questions):
        correct  = str(q.get("answer", "")).strip()
        expl     = q.get("explanation", "")
        options  = q.get("options") or []
        user_sel = str(user_answers.get(i, "")).strip()

        is_correct = (
            user_sel[:2].upper() == correct[:2].upper()
            or correct.lower() in user_sel.lower()
        )

        pdf.put_text(f"Q{i + 1}. {q.get('question', '')}", bold=True)
        pdf.ln(1)

        for opt in options:
            opt_s = str(opt).strip()
            is_opt_ans  = (opt_s[:2].upper() == correct[:2].upper()
                           or correct.upper() in opt_s.upper()
                           or opt_s.upper() == correct.upper())
            is_opt_user = (opt_s == user_sel)

            pdf.set_x(L_M + 6)
            if is_opt_ans:
                pdf.set_font("Helvetica", "B", 10)
                pdf.set_text_color(0, 130, 0)
                pdf.multi_cell(pdf.ew - 6, 6, f"[CORRECT] {_safe(opt_s)}")
            elif is_opt_user and not is_opt_ans:
                pdf.set_font("Helvetica", "B", 10)
                pdf.set_text_color(180, 0, 0)
                pdf.multi_cell(pdf.ew - 6, 6, f"[YOUR ANSWER - WRONG] {_safe(opt_s)}")
            else:
                pdf.set_font("Helvetica", "", 10)
                pdf.set_text_color(80, 80, 80)
                pdf.multi_cell(pdf.ew - 6, 6, f"   {_safe(opt_s)}")
            pdf.set_text_color(30, 30, 30)

        pdf.ln(2)
        # Result badge (cell only — no multi_cell, safe for single line)
        pdf.set_x(L_M)
        if is_correct:
            pdf.set_font("Helvetica", "B", 10)
            pdf.set_fill_color(220, 255, 220)
            pdf.set_text_color(0, 120, 0)
            pdf.cell(pdf.ew, 7, "  CORRECT", ln=True, fill=True)
        else:
            pdf.set_font("Helvetica", "B", 10)
            pdf.set_fill_color(255, 220, 220)
            pdf.set_text_color(160, 0, 0)
            label = f"  INCORRECT  |  Correct: {_safe(correct[:60])}"
            pdf.cell(pdf.ew, 7, label, ln=True, fill=True)

        if expl:
            pdf.ln(1)
            pdf.put_note(f"Explanation: {expl}", color=(60, 60, 60))

        pdf.set_text_color(30, 30, 30)
        pdf.ln(4)
        pdf.put_hr()

    return bytes(pdf.output())


# ═══════════════════════════════════════════════════════════════════════════════
# Mock Test — Paper PDF
# ═══════════════════════════════════════════════════════════════════════════════

def generate_mock_paper_pdf(exam: dict[str, Any]) -> bytes:
    """Clean exam paper PDF — questions only, no answers."""
    pdf = _BasePDF()
    pdf.add_page()
    pdf.put_title(
        exam.get("title", "Mock Examination Paper"),
        f"Total Marks: {exam.get('total_marks', 0)}"
    )

    section_letter = "A"
    for section in exam.get("sections", []):
        q_type     = section["type"]
        marks_each = section["marks_each"]
        sec_total  = section["total_marks"]

        pdf.put_section(
            f"SECTION {section_letter} — {q_type.upper()}  "
            f"[{marks_each} mark(s) each | Section Total: {sec_total} marks]"
        )

        for i, q in enumerate(section.get("questions", []), 1):
            pdf.put_text(f"Q{i}. {q.get('question', '')}", bold=True)
            for opt in (q.get("options") or []):
                pdf.put_text(f"     {opt}", indent=6, color=(60, 60, 60))
            pdf.ln(4)

        pdf.ln(3)
        section_letter = chr(ord(section_letter) + 1)

    return bytes(pdf.output())


# ═══════════════════════════════════════════════════════════════════════════════
# Mock Test — Answer Key PDF
# ═══════════════════════════════════════════════════════════════════════════════

def generate_mock_answerkey_pdf(exam: dict[str, Any]) -> bytes:
    """Professional answer key PDF with full explanations per question."""
    pdf = _BasePDF()
    pdf.add_page()
    pdf.put_title("Answer Key", exam.get("title", "Mock Examination Paper"))

    section_letter = "A"
    for section in exam.get("sections", []):
        q_type     = section["type"]
        marks_each = section["marks_each"]

        pdf.put_section(
            f"SECTION {section_letter} — {q_type}  [{marks_each} mark(s) each]"
        )

        for i, q in enumerate(section.get("questions", []), 1):
            # Question
            pdf.set_x(L_M)
            pdf.set_font("Helvetica", "B", 10)
            pdf.set_text_color(30, 30, 80)
            pdf.multi_cell(pdf.ew, 6, f"Q{i}. {_safe(q.get('question', ''))}")
            pdf.ln(1)

            # Answer
            pdf.set_x(L_M)
            pdf.set_font("Helvetica", "B", 10)
            pdf.set_text_color(0, 128, 0)
            pdf.multi_cell(pdf.ew, 6, f"Answer: {_safe(q.get('answer', 'N/A'))}")

            # Explanation
            expl = q.get("explanation", "")
            if expl:
                pdf.set_x(L_M)
                pdf.set_font("Helvetica", "I", 9)
                pdf.set_text_color(60, 60, 60)
                pdf.multi_cell(pdf.ew, 5, f"Explanation: {_safe(expl)}")

            pdf.set_text_color(30, 30, 30)
            pdf.ln(4)
            pdf.put_hr()

        section_letter = chr(ord(section_letter) + 1)

    return bytes(pdf.output())
