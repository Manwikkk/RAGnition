# 2. Quiz Result PDF (with user answers)
# ═══════════════════════════════════════════════════════════════════════════════

def generate_quiz_result_pdf(
    questions: list[dict[str, Any]],
    user_answers: dict[int, str],
    score: int,
    total: int,
) -> bytes:
    pdf = _PDF()
    pdf.add_page()
    pct = int(score / total * 100) if total else 0
    pdf.title_block("Quiz Results", f"Score: {score} / {total}   ({pct}%)")

    for i, q in enumerate(questions):
        correct    = str(q.get("correct_answer", q.get("answer",""))).strip().upper()
        opts       = _opts_list(q.get("options"))
        user_letter = str(user_answers.get(i, "")).strip().upper()[:1]
        is_correct = (user_letter == correct[:1])
        expl       = q.get("explanation", "")

        pdf.body(f"Q{i+1}. {q.get('question','')}", bold=True)
        pdf.ln(1)

        for letter, text in opts:
            is_ans  = (letter == correct[:1])
            is_user = (letter == user_letter)
            pdf.set_x(L_M + 8)
            if is_ans:
                pdf.set_font("Helvetica", "B", 10)
                pdf.set_text_color(0, 130, 0)
                pdf.multi_cell(pdf.ew - 8, 7, f"[CORRECT] {letter}. {_safe(text)}")
            elif is_user and not is_ans:
                pdf.set_font("Helvetica", "B", 10)
                pdf.set_text_color(180, 0, 0)
                pdf.multi_cell(pdf.ew - 8, 7, f"[YOUR ANSWER - WRONG] {letter}. {_safe(text)}")
            else:
                pdf.set_font("Helvetica", "", 10)
                pdf.set_text_color(80, 80, 80)
                pdf.multi_cell(pdf.ew - 8, 7, f"   {letter}. {_safe(text)}")
            pdf.set_text_color(30, 30, 30)

        pdf.ln(2)
        if is_correct:
            pdf.fill_row("  CORRECT", fill_color=(220, 255, 220), text_color=(0, 110, 0))
        else:
            pdf.fill_row(f"  INCORRECT  |  Correct: {correct[:1]}", fill_color=(255, 220, 220), text_color=(150, 0, 0))

        if expl:
            pdf.ln(1)
            pdf.set_x(L_M + 8)
            pdf.set_font("Helvetica", "I", 9)
            pdf.set_text_color(60, 60, 60)
            pdf.multi_cell(pdf.ew - 8, 5, f"Explanation: {_safe(expl)}")
            pdf.set_text_color(30, 30, 30)

        pdf.ln(4)
        pdf.hr()

    return bytes(pdf.output())


