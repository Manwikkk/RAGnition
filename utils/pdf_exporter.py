"""
utils/pdf_exporter.py
---------------------
Generates PDF documents for RAGnition.

PDFs:
  - generate_questions_pdf()   — clean question paper with correct answers marked
  - generate_mock_paper_pdf()  — exam paper (questions only, no answers)
  - generate_mock_answerkey_pdf() — professional answer key with full explanations
"""

from fpdf import FPDF
from typing import Any

# Fixed A4 margins
L_M, R_M, T_M = 18, 18, 22


def _safe(text: str) -> str:
    """Strip/replace all non-latin-1 characters so FPDF never crashes."""
    if not text:
        return ""
    replacements = {
        "\u2014": "-", "\u2013": "-", "\u2018": "'", "\u2019": "'",
        "\u201c": '"', "\u201d": '"', "\u2022": "*", "\u2026": "...",
        "\u00b2": "2", "\u00b3": "3", "\u00b0": "deg",
        "\u03b1": "alpha", "\u03b2": "beta", "\u03c0": "pi",
        "\u2264": "<=", "\u2265": ">=", "\u2260": "!=",
        "\u00e9": "e", "\u00e8": "e", "\u00ea": "e",
        "\u00e0": "a", "\u00e2": "a", "\u00f4": "o",
        "\u00fb": "u", "\u00fc": "u", "\u00ef": "i",
    }
    for k, v in replacements.items():
        text = text.replace(k, v)
    # Final fallback: encode to latin-1, replace anything unmatched with ?
    return text.encode("latin-1", errors="replace").decode("latin-1")


class _BasePDF(FPDF):
    """Base class with consistent margins, header, footer, and helper methods."""

    def __init__(self, title: str = "RAGnition"):
        super().__init__()
        self._doc_title = title
        self.set_margins(L_M, T_M, R_M)
        self.set_auto_page_break(auto=True, margin=20)

    @property
    def ew(self) -> float:
        """Effective usable width."""
        return self.w - L_M - R_M

    def header(self):
        self.set_font("Helvetica", "B", 9)
        self.set_text_color(102, 126, 234)
        self.set_x(L_M)
        self.cell(self.ew, 6, "RAGnition  |  AI Powered Exam Preparation", align="L", ln=True)
        self.set_draw_color(102, 126, 234)
        self.set_line_width(0.4)
        self.line(L_M, self.get_y(), self.w - R_M, self.get_y())
        self.ln(5)

    def footer(self):
        self.set_y(-14)
        self.set_font("Helvetica", "I", 8)
        self.set_text_color(150, 150, 150)
        self.set_x(L_M)
        self.cell(self.ew, 8, f"Page {self.page_no()}  |  Generated by RAGnition", align="C")

    # ── Layout helpers ────────────────────────────────────────────────────────

    def put_title(self, title: str, subtitle: str = ""):
        self.set_x(L_M)
        self.set_font("Helvetica", "B", 16)
        self.set_text_color(102, 126, 234)
        self.multi_cell(self.ew, 9, _safe(title), align="C")
        if subtitle:
            self.set_x(L_M)
            self.set_font("Helvetica", "", 10)
            self.set_text_color(100, 100, 100)
            self.multi_cell(self.ew, 6, _safe(subtitle), align="C")
        self.ln(5)
        self.set_text_color(30, 30, 30)

    def put_section(self, text: str):
        self.ln(3)
        self.set_x(L_M)
        self.set_font("Helvetica", "B", 11)
        self.set_text_color(102, 126, 234)
        self.multi_cell(self.ew, 7, _safe(text))
        self.set_draw_color(180, 180, 255)
        self.set_line_width(0.3)
        self.line(L_M, self.get_y(), self.w - R_M, self.get_y())
        self.ln(4)
        self.set_text_color(30, 30, 30)

    def put_text(self, text: str, bold: bool = False,
                 color: tuple = (30, 30, 30), size: int = 10, indent: float = 0):
        self.set_x(L_M + indent)
        self.set_font("Helvetica", "B" if bold else "", size)
        self.set_text_color(*color)
        self.multi_cell(self.ew - indent, 6, _safe(text))
        self.set_text_color(30, 30, 30)

    def put_note(self, text: str, color: tuple = (70, 70, 70), indent: float = 6):
        self.set_x(L_M + indent)
        self.set_font("Helvetica", "I", 9)
        self.set_text_color(*color)
        self.multi_cell(self.ew - indent, 5, _safe(text))
        self.set_text_color(30, 30, 30)

    def put_hr(self):
        self.set_draw_color(220, 220, 220)
        self.set_line_width(0.3)
        self.line(L_M, self.get_y(), self.w - R_M, self.get_y())
        self.ln(4)


