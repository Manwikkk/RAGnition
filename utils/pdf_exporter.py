# 4. Mock Test Answer Key PDF — full explanations
# ═══════════════════════════════════════════════════════════════════════════════

def generate_mock_answerkey_pdf(exam: dict[str, Any]) -> bytes:
    pdf = _PDF()
    pdf.add_page()
    pdf.title_block("Answer Key", exam.get("title", "Mock Examination Paper"))

    section_letter = "A"
    for section in exam.get("sections", []):
        q_type     = section["type"]
        marks_each = section["marks_each"]

        pdf.section_header(
            f"SECTION {section_letter}  —  {q_type}   [{marks_each} mark(s) each]"
        )

        for i, q in enumerate(section.get("questions", []), 1):
            answer = _answer_of(q)
            expl   = q.get("explanation", "")

            pdf.body(f"Q{i}. {q.get('question','')}", bold=True, color=(30, 30, 80))
            pdf.ln(1)
            pdf.indent(f"Answer: {answer}", color=(0, 120, 0), bold=True)
            if expl:
                pdf.set_x(L_M + 8)
                pdf.set_font("Helvetica", "I", 9)
                pdf.set_text_color(60, 60, 60)
                pdf.multi_cell(pdf.ew - 8, 5, f"Explanation: {_safe(expl)}")
                pdf.set_text_color(30, 30, 30)

            pdf.ln(4)
            pdf.hr()

        section_letter = chr(ord(section_letter) + 1)

    return bytes(pdf.output())
